        <h1 class="text-dark text-center">About StrawPoll</h1>
<p>StrawPoll provides a platform for users to create various types of polls. The most popular type are "straw polls", which can be used to easily find the majority in a group with different opinions. The name "straw poll" was embossed in web culture since Usenet (since 1979) as a synonym for this type of ad-hoc, unofficial votes. We do our best to fight voting fraut with various methods (read below), but we cannot guarantee to completely eliminate it. Therefore, our "straw polls" shouldn't be used in political context, in which the right term of this type of polls is "honor-system polls".
Online Polls for More Than Ten Years</p>
<p>It all began with strawpollnow.com, a site that started a Twitter polling service in around 2007. Ahead of it's time, it somehow wasn't the real breakthrough in online polls. In 2014, we launched strawpoll.de with a fresh redesign in our home country, Germany. It quickly conquered the world with a easy-to-use and ad-free, multi-langual site. A lot of people even outside of Germany used the german based website, so we thought about our options to go international. It took some time to negotiate, but finally in early 2017, we acquired strawpoll.com to give our international, years-old business an address it deserves.
User Experience Matters</p>
<p>Truth to be told, there are plenty of online polling services around the web. To be different (and better) than others, we try to provide the best experience, easy navigation and fast sharing possibilities. Just to make sure you and your voters are always happy to use our service. In every step from creating to sharing to evaluation of your polls, we are optimizing the process from scratch. If you think anything could be done better, we are always open to hear from you!
Fighting Voter Fraud Matters Even More
The most important part of running a successful online polling platform is fighting vote rigging at all fronts. After getting voting bots and script under control, the next big thing was changing IP-adresses via VPN or proxies. We're happy to announce that we have implemented a VPN detection system in our backend to lock out all the fake votes from VPN users. Keep polling!
